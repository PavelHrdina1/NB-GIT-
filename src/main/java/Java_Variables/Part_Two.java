package Java_Variables;

  public class Part_Two {
      
      public static void main(String[] args){
          
          // Double variables
          
           double minute, hour;
          
          // Double variables can hold very large (or small) numbers
          
           minute = 10.5;
           hour = 20.8;
          
          // Maximum values are 17 followed by 307 zeros
          // They are olso used to hold floating numbers (1.1, 1.2 ...)
          
          System.out.println(minute + hour);
          
          // Output = 31.3
        
      }
}
